import React from 'react';
import {
  BrowserRouter,
  Route,
} from 'react-router-dom';

import UserPage from './components/UserPage';
import StateSelect from './components/StateSelect';
import Navbar from './components/Navbar';
import CitySelect from './components/CitySelect';
import PlacesSearch from './components/PlacesSearch';

const Router = ({
  addTrip,
  trips,
  states,
  currentState,
  setCurrentState,
}) => (
  <BrowserRouter>
    <div>
      <Navbar />

      <Route
        name="Homepage"
        exact path="/"
        render={props => (
          <UserPage
            trips={trips}
            {...props}
            />
        )}
      />

      <Route
        name="stateSelect"
        path="/stateSelect"
        render={props => (
          <StateSelect
            states={states}
            currentState={currentState}
            setCurrentState={setCurrentState}
            {...props}
          />
        )}
      />

      <Route
        name="citySelect"
        path="/citySelect"
        render={props => (
          <CitySelect
            states={states}
            currentState={currentState}
            {...props}
          />
        )}
      />
      <Route
        name="placesSearch"
        path="/places-search"
        component={PlacesSearch}
      />
    </div>
  </BrowserRouter>
);

export default Router;
